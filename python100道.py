# 问题6 ***
# 编写一个程序，根据给定的公式计算并打印该值：
# Q = [（2 _ C _ D）/ H]的平方根
# 以下是C和H的固定值：
# C为50。H为30。
# D是变量，其值应以逗号分隔的顺序输入到您的程序中，例如，让我们假设以下逗号分隔的输入顺
# 序被赋予了程序：
# 100,150,180
# 该程序的输出应为
# 18,22,24

# 调用包
from math import sqrt
# 1.根据题意，设置对应变量
C = 50
H = 30


# 2. 定义计算函数，计算 Q = [（2 _ C _ D）/ H]的平方根
def calc(d):
    return sqrt((2 * C * d) / H)


# 3.定义输入值变量
# D = input()
D = '100,150,180'
# 4.拆分字符串成列表
d = D.split(',')
# 5. 调用cal函数计算，把计算后的结果通过逗号连接起来
Q = ','.join([str(round(calc(int(i)))) for i in d])
# 6. 输出
print(Q)

