# 问题7
# _编写一个程序，该程序将X，Y两位数字作为输入并生成一个二维数组。数组的第i行和第j列中的
# 元素值应为i _ j。*
# 注意：i = 0,1 ..，X-1; j = 0,1，¡Y-1。假设将以下输入提供给程序：3,5
# 然后，程序的输出应为：
# [[0, 0, 0, 0, 0], [0, 1, 2, 3, 4], [0, 2, 4, 6, 8]]

# 1.设置输入值变量
# input1 = input('输入内容：')
input7 = '3,5'
# 2.按逗号分割字符串，并且把值转换成整数，然后把值赋予给对应的变量
x, y = map(int, input7.split(','))  # 第一次使用map,后续可以多复习复习 ***
# 3.定义一个新的空列表，以便后续存储结果值
result7 = []
# 4.外层遍历循环
for i in range(0, x):
    # 5.定义新列表，存储二维遍历的元素值
    list7 = []
    # 6.二维元素遍历
    for j in range(0, y):
        # 7.把计算值遍历存入到上层列表中
        list7.append(i * j)
    # 8.把二维元素值放入到外层列表
    result7.append(list7)
# 9.输出结果
print(result7)

